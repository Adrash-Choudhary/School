<h2>Swimming Pool</h2>
<p><b>GPS Saharsa,</b> has Half-Olympic Sized Swimming Pool, which is one of the largest swimming pools in Bihar/Jharkhand. A toddler pool, meant for the Junior School children, is also present.</p>
<p>Swimming is useful to children in many ways. It strengthens the shoulder and thigh muscles and also allows children to breathe deeply, and sustain breath under water for long periods of time. Students are provided specific time slots and weekend slots for practice of swimming.</p>
                                
<p>Encouraging students to swim can also improve their mental and emotional health. The natural buoyancy of the water is more relaxing than other types of exercise. Swimming can also improve overall mood, and help combat depression. Learning to swim, and being confident and familiar with water, is important for everyone. Swimming skills, particularly among children, are fundamental to every individual's safety to ensure they have the ability to survive in and around the water.
</p>
<p>In <b>GPS Saharsa,</b> swimming is conducted by trained coaches and is supervised carefully for the safety of each and every student involved in the activity. We are extremely fortunate to have this facility and our aim is to ensure that it is available for use by both the school and local community.
</p>
	
