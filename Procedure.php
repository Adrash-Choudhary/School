<h3 style="color:#202C45">Procedure</h3>
<hr>
 

<p>
After the Observation/Interaction/Assessment, a list of all successful candidates will be displayed on the notice board of the school. The parents of the successful candidates will be required to contact the school office (9431626967) for completion of the admission formalities by depositing the required fee and other charges along with the necessary documents. 
</p>

<h4>Documents to be submitted at the time of Admission: </h4>
<p>
<b>1.</b> School leaving/transfer certificate and previous year's Report Card (without this, No student will be admitted).<br>
<b>2.</b> Medical Certificate in the given format.<br>
<b>3.</b> Copy of Aadhar Card of the Candidate.<br>
<b>4.</b> Birth Certificate, in original, to be brought for verification only.
Recent passport size colour photograph of the candidate and both the parents/guardian.<br>
<b>5.</b> Admission will be cancelled if any variance is found between the particulars filled in the Registration Form and the documents submitted for admission. 
</p>




<h4>OBSERVATION-CUM-INTERACTION FOR CLASSES PRE-NURSERY, NURSERY, PREPARATORY AND CLASS I AND CLASS II</h4>
<p>Admission to Pre-Nursery, Nursery, Preparatory, Class I and Class II will be granted on the basis of the Observation-cum-Interaction. The presence of both the parents during this period is mandatory. Kindly note that the school does not take any kind of interview for admission to the primary classes. <br><br>
The parents will have to bring both the copies of Admit Card on the date and time mentioned on the cards, when they come for the Observation-cum-Interaction. The Admit Card will have to be handed over to the Teacher In-charge for Observation-cum-Interaction and the Parent's copy will be retained by the parents to collect their child after the Observation Session. </p>



<h4>AGE CRITERIA</h4>
<p>The eligibility criterion for various classes is based on the age of the child. For the next academic year the applicant's age as on 31st March 2023, as recorded in the birth certificate will determine the eligibility for admission to a particular class.</p>

