<h4>ASSESSMENT AND INTERACTION SESSION FOR CLASS III TO IX</h4> 
<p>Class III onwards, an assessment will be conducted, followed by an Interactive Session.  Both the parents must be present in the School on the designated day(s).<br>
The parents must bring both the copies of the Admit Card on the date and time mentioned on the Admit Cards, when they bring their ward(s) to school.<br>
The entire procedure may last for three to four hours. Parents are advised to plan their schedules accordingly.  <br>
Before the commencement of the assessment, parents must hand over the student's copy of the Admit Card to the Session-In-Charge. <br>
<br> 
<i><b>Disclaimer</b>: The School Management reserves the right to add, alter, amend or cancel any of the rules, procedures prevailing at any time which will be binding on the parents / guardians and students.</i>
</p>

